#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int x;

	x = n;
	if (x == -2147483648)
	{
		write(fd, "-2147483648", 11);
	}
	else if (n < 0)
	{
		ft_putchar_fd('-', fd);
		x = x * -1;
		ft_putnbr_fd(x, fd);
	}
	else if (x > 9)
	{
		ft_putnbr_fd(x / 10, fd);
		ft_putnbr_fd(x % 10, fd);
	}
	else
		ft_putchar_fd(x + '0', fd);
}

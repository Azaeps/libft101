#include "libft.h"

void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int x;

	x = nb;
	if (x == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else if (nb < 0)
	{
		ft_putchar('-');
		x = x * -1;
		ft_putnbr(x);
	}
	else if (x > 9)
	{
		ft_putnbr(x / 10);
		ft_putnbr(x % 10);
	}
	else
		ft_putchar(x + '0');
}

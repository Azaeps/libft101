#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	x;
	char			*str;

	x = 0;
	if (s == NULL)
		return (NULL);
	while (s[x])
		x++;
	if (!(str = (char *)malloc(sizeof(char) * (x + 1))))
		return (NULL);
	x = 0;
	while (s[x])
	{
		str[x] = f(x, s[x]);
		x++;
	}
	str[x] = '\0';
	return (str);
}
